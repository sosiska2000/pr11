using System; // Использование базовых классов .NET (не используется явно, но для базовой функциональности)
using System.Collections.Generic; // Использование коллекций List<T>
using System.Linq; // Использование LINQ-методов (хотя в данном классе не используется явно)
using System.Text; // Использование классов для работы с кодировкой (не используется явно в этом файле)
using System.Threading.Tasks; // Использование асинхронного программирования (не используется явно в этом файле)

namespace APIGigaChatImage.Models.Response // Пространство имен для моделей ответов API генерации изображений
{
    // Основной класс для десериализации ответа от API генерации изображений GigaChat
    // Этот класс соответствует структуре JSON-ответа от конечной точки генерации изображений
    public class ImageGenerationResponse
    {
        // Временная метка создания ответа в формате Unix time (секунды с 1 января 1970 года)
        // Тип long используется для точного хранения больших временных значений
        // Позволяет отследить, когда именно было сгенерировано изображение
        public long created { get; set; }

        // Список сгенерированных изображений
        // Каждый элемент списка представляет одно изображение
        // API может генерировать несколько изображений за один запрос
        public List<ImageData> data { get; set; }

        // Вложенный класс, представляющий данные одного сгенерированного изображения
        public class ImageData
        {
            // URL-адрес сгенерированного изображения
            // Это ссылка, по которой можно скачать или просмотреть изображение
            // Обычно это временная ссылка, действительная в течение ограниченного времени
            public string url { get; set; }

            // Уникальный идентификатор сгенерированного изображения
            // Может использоваться для:
            // - Отслеживания изображения в системе
            // - Повторного запроса изображения
            // - Логирования и отладки
            public string id { get; set; }
        }
    }
}