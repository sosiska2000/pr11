using System; // Использование базовых классов .NET (не используется явно, но для базовой функциональности)
using System.Collections.Generic; // Использование коллекций List<T> (хотя в этом классе не используется)
using System.Linq; // Использование LINQ-методов (хотя в данном классе не используется явно)
using System.Text; // Использование классов для работы с кодировкой (не используется явно в этом файле)
using System.Threading.Tasks; // Использование асинхронного программирования (не используется явно в этом файле)

namespace APIGigaChatImage.Models.Response // Пространство имен для моделей ответов API работы с изображениями
{
    // Класс для десериализации информации о статусе и деталях изображения от API GigaChat
    // Используется при запросе информации о уже сгенерированном изображении или проверке статуса
    public class ImageInfoResponse
    {
        // Уникальный идентификатор изображения в системе GigaChat API
        // Этот ID используется для:
        // - Отслеживания конкретного изображения
        // - Запроса дополнительной информации о изображении
        // - Ссылки на изображение в последующих запросах
        public string id { get; set; }

        // URL-адрес изображения для доступа или загрузки
        // Это может быть:
        // - Прямая ссылка на файл изображения
        // - Ссылка на CDN (Content Delivery Network)
        // - Временная ссылка с ограниченным сроком действия
        public string url { get; set; }

        // Текущий статус обработки или доступности изображения
        // Возможные значения (зависят от реализации API):
        // - "processing": изображение все еще генерируется/обрабатывается
        // - "completed": изображение успешно сгенерировано и доступно
        // - "failed": произошла ошибка при генерации изображения
        // - "expired": срок действия ссылки на изображение истек
        // - "available": изображение доступно для загрузки
        public string status { get; set; }
    }
}